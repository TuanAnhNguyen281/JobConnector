package controller;

import dal.AdminDAO;
import jakarta.servlet.RequestDispatcher;
import java.io.IOException;
import java.io.PrintWriter;
import jakarta.servlet.ServletException;
import jakarta.servlet.http.HttpServlet;
import jakarta.servlet.http.HttpServletRequest;
import jakarta.servlet.http.HttpServletResponse;
import java.util.List;
import model.Account;

/**
 *
 * @author My PC
 */
public class AccountListServlet extends HttpServlet {

    private static final long serialVersionUID = 1L;

    protected void processRequest(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        AdminDAO accountDao = new AdminDAO();
        List<Account> accounts = accountDao.getAllAccounts();

// Kiểm tra xem danh sách có dữ liệu không
        System.out.println("Accounts size: " + accounts.size());

// Đưa dữ liệu vào request để sử dụng trong JSP
        request.setAttribute("accounts", accounts);

// Chuyển hướng tới trang JSP để hiển thị thông tin
        RequestDispatcher dispatcher = request.getRequestDispatcher("accountHome.jsp");
        dispatcher.forward(request, response);
    }

    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    @Override
    public String getServletInfo() {
        return "Short description";
    }// </editor-fold>

}
